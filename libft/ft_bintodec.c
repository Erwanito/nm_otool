#include "libft.h"

int	ft_bintodec(int bin)
{
	int	dec;
	int	c;

	dec = 0;
	c = 0;
	while (bin != 0)
	{
		dec = dec + (ft_power(10, c) * (bin % 2));
		bin /= 2;
		c++;
	}
	return (dec);
}
